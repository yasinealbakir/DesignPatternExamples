package BuilderPattern.Example;

public class Car {
    private String brand;
    private String model;
    private int year;

    private String color;

    private String fuelType;

    public Car(CarBuilder builder) {
        this.brand = brand;
        this.model = model;
        this.year = year;
        this.color = color;
        this.fuelType = fuelType;
    }




}
